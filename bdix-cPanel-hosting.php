        <h2 py-4>cPanel :: Advantages - Disadvantages</h2>

        <h3 class="pt-5">Advantages:</h3>
        <p>- Easy to navigate,  colorful,  and vibrant client interface:<br>

          cPanel offers a beautiful client-side interface.  It is extremely easy to navigate,  with an icon-based menu on the main page,  linking the user to the important features which the control panel offers.<br><br>

          - Wide array of supported operating systems,  including Redhat,  Free BSD,  CentOS,  Fedora,  Debian,  and Trustix:<br>

          cPanel has been developed to offer support for many of the most popular operating systems for web servers.  This allows the control panel to be utilized by almost every webhosting company.  The developers of cPanel are also working on versions which will be compatible with the Windows and Mac OSX operating systems.<br><br>

          - Widely accepted by potential webhosting clients:<br>

          cPanel is the biggest name in the webhosting control panel industry,  and is recognized by almost all potential webhosting clients.  This gives the client a sense of familiarity,  and also gives the client piece of mind,  as they know that they are getting a stable control panel system.<br><br>

          - Relatively low-cost solution,  if purchased through a datacenter:<br>

          Various dedicated server providers,  such as EV1 Servers,  Layered Technologies and CyberWurx have partnerships with cPanel which allows them to get low-cost licenses for their dedicated server clients.  cPanel can be added to servers,  in most cases,  for as low as $25.00/month,  which is a far cry from the hundreds of dollars it would cost to purchase a license direct from cPanel.<br><br>

          - Various addons to the control panel,  such as cPanel PRO and Fantastico available:<br>

          cPanel has made it easy to incorporate third-party applications into their control panel software.  The most popular cPanel addon is,  without a doubt Fantastico,  developed by Netenberg.  Fantastico is an auto installer program,  which allows the hosting client to set-up scripts from a library of over 50 within seconds.

          cPanel PRO is an addon for the cPanel control panel,  released and developed by cPanel,  Inc.  cPanel PRO offers various addons to the control panel,  including a built-in support submission form.<br><br>

          Easily skinnable,  many skins available:<br>

          cPanel is an extremely easy control panel to skin,  and many design and skinning companies have taken advantage of this.  Skins such as iCandy and cPanel XP 2004 provide an amazing interface design,  which can set your company apart from the competition.  In addition,  skin sets such as cPanel XP 2004 and RV Skin take advantage of the WHM API,  and incorporate various features to the control panel,  including custom pages.<br><br>
          - Frequent software updates:<br>

          cPanel releases updates to their software quite often.  Many times,  upgrades are available daily,  or on an every other day basis.  This proves cPanel,  Inc’s dedication to providing a quality control panel product. </p>

          <h3 class="pt-4">Disadvantages:</h3>
          <p>- Different layout for WHM,  the reseller control panel,  which can make usage difficult for the user:<br>

          Web Host Manager,  cPanel’s reseller control panel,  uses a different layout than that of the cPanel interface.  The          WHM offers a framed layout,  with an elongated navigation on the left,  and an icon-based menu in the right window.           Although navigation in the Web Host Manager interface is incredibly easy,  it can prove to be confusing for some        resellers.  Many other control panel softwares use the same base layout/design for both the reseller control panel          and the end-user panel.<br><br>

          - Interface is not extremely professional:<br>

          Compared to the interfaces of several other control panels,  the cPanel control panel is not very professional.  Although it is easy to use,  it can be viewed as too much of an ‘eye candy’ interface.  Many webhosts and customers alike believe that more time could be spent developing new features for the control panel,  than by creating a design which is not entirely professional,  and in some cases,  overdone.<br><br>

          - Used by MANY webhosts,  therefore the control panel does not set your business apart from the rest:<br>

          Unlike other control panels,  cPanel is the most widely used in the webhosting industry.  Thus,  the competition between cPanel webhosts,  and the market within is much more flooded than the markets of other control panels.  A cPanel webhost has much less of a chance of being chosen by a potential webhosting customer,  than a company which uses Ensim.<br><br>

          - mainly because the Ensim market is much less saturated.<br>

          cPanel is an extremely powerful control panel.  It’s advantages far outweigh it’s disadvantages,  as you can deduce from the above.  As we know,  a company’s control panel choice is based solely upon an opinion.  cPanel can not be considered the best because it is the most widely used - there are many comparable control panels available on the market,  and the term ‘best’ is simply a matter of opinion.  Regardless,  cPanel is an excellent piece of webhosting software,  and it would most certainly be an excellent choice. </p>
